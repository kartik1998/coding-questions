package CodeChef;

import java.util.Scanner;

public class TTUPLE {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t=sc.nextInt();
        while(t-->0){
            int x = sc.nextInt() , y = sc.nextInt() , z = sc.nextInt();
            int X = sc.nextInt() , Y = sc.nextInt() , Z = sc.nextInt();
            if(x==X && y==Y && z==Z){
                System.out.println(0);
                continue;
            }
            
        }
    }
}
