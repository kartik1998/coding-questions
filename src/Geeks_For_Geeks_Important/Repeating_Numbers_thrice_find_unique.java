package Geeks_For_Geeks_Important;

import java.util.Scanner;

// Find the element that appears once in array but other elements appear thrice
public class Repeating_Numbers_thrice_find_unique {

  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    int N = sc.nextInt();
    int A[] = new int[N];
    for (int i = 0; i < N; i++) {
      A[i] = sc.nextInt();
    }
  }
}
